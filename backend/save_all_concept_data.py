# 解析函数
def parse_html(html, date_str):
    pattern = r'(\d+)\s+(\d{6})([^\d,]+?)(\d+(?:,\d+)*(?:\.\d+)?)(-?\d+(?:\.\d+)?)\d+/\d+(\d+(?:,\d+)*)'
    matches = re.findall(pattern, html)
    
    result = {}
    for match in matches:
        rank, code, name, close, change, amount = match
        close = float(close.replace(',', ''))
        change = float(change)
        result[code] = {
            'name': name,
            'close': close,
            'change': change
        }
        if code not in all_concepts:
            all_concepts[code] = name
    
    print(f"  {date_str}: 找到 {len(result)} 条数据")
    return result

